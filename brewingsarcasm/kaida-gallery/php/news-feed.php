            <p><span class="courier-span">To the one who always gets away....</span></p>
            <br>
            <p><span class="news-title">Me, Honey</span>
            <br>
                <span class="courier-span">March 21, 2017</span>
            </p>
            <br>
            <p><span class="section-text">
            You know the drill.<br>
            countless times I would wait,<br>
            Stoically restless, humbled,<br>
            by the grandeur of this towering steel gate<br>
            as though an unnerving reminder<br>
            of my puny existence,<br>
            subdued by the Spanish lines and curves,<br>
            passing posh cuddly dogs,<br>
            and that first glimpse of you walking passively<br>
            as though I wasn’t here.<br>
            At last, I no longer have to wonder<br>
            about what you’re wearing<br>
            or whether your sweet welcome smile<br>
            still matches the last one I kept.<br>
            Those few steps you take<br>
            before we awkwardly bump our cheeks<br>
            rolled over so slowly, gently,<br>
            as the long days tumbled<br>
            towards this very moment.<br>
            <br>
            Japanese or Italian? Coffee or beer?<br>
            Did it even matter?<br>
            I just wanted to catch glances with you.<br>
            I just wanted to read from your eyes the reasons<br>
            why you waste time with me.<br>
            Perhaps, you like listening to my silence,<br>
            smothering sugar on my bitter thoughts.<br>
            Or, perhaps, I am a fleeting cure for dullness.<br>
            Does it even matter?<br>
            We could throw random musings<br>
            out of nowhere<br>
            or dice-up your ex-boyfriend on mid-air.<br>
            <br>
            You had me when you gasp in boredom and exasperation.<br>
            You had me with your lengthy, tipsy monologues<br>
            and the way you carefully carved out<br>
            the shattered pieces of my soul,<br>
            frozen and fossilized deep in the chasms of indifference,<br>
            masterfully crafted them back together in your hands.<br>
            <br>
            I wonder if we’re just a skipping heartbeat,<br>
            a sudden thump in an otherwise quiet winding road.<br>
            Maybe. Maybe not.<br>
            But I think I could exchange glances with you forever.<br>
            Thai or Chinese.<br>
            It doesn’t really matter.<br>
